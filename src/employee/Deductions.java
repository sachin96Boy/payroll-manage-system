    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String first_name = txtFirstname.getText();
        String sur_name = txtSurname.getText();
        String salary = txtSalary.getText();
        String total_deduc = lblTotalDeduction.getText();
        String reason = txtReason.getText();
        String employee_id = txtEmployeeId.getText();
        String loggedInAs = lblLogedInAs.getText();
        String userId=txtSearchBar.getText();

        try {
            String sql = "INSERT INTO deductions( user_id,FirstName,Surname,Salary,deduction_amount,deduction_reason,id,made_by)VALUES(?,?,?,?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            pst.setString(1, userId);
            pst.setString(2, first_name);
            pst.setString(3, first_name);
            pst.setString(4, sur_name);
            pst.setString(5, salary);
            pst.setString(6, total_deduc);
            pst.setString(7, reason);
            pst.setString(8, employee_id);
            pst.setString(9, loggedInAs);

            pst.execute();
            JOptionPane.showMessageDialog(null, "Deductions Successfully recorded");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

