    private void txtSearchBarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchBarKeyReleased
        String id = txtSearchBar.getText();

        try {
            String sql = "SELECT * FROM staff_info WHERE id =?";
            pst = conn.prepareStatement(sql);

            pst.setString(1, id);

            rs = pst.executeQuery();
            while (rs.next()) {
                txtEmployeeId.setText(rs.getString("id"));
                txtFirstname.setText(rs.getString("Fname"));
                txtSurname.setText(rs.getString("Sname"));
                txtDateOfBirth.setText(rs.getString("DoB"));
                txtSalary.setText(rs.getString("BasicS"));
                txtDepartment.setText(rs.getString("department"));
                txtDesignation.setText(rs.getString("Designation"));
                txtDateOfHire.setText(rs.getString("DateH"));
                txtJobTitle.setText(rs.getString("JobT"));
                txtStatus.setText(rs.getString("Status"));
            }
            // TODO add your handling code here:
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_txtSearchBarKeyReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int Salary = Integer.parseInt(txtSalary.getText());
        int deduction;
        double totaldeduction;
        int salaryAfter = 0;

        if (RadioPercentage.isSelected() == true) {
            deduction = Integer.parseInt(txtRadioPercentage.getText());

            totaldeduction = (deduction * 0.01 * Salary);

            lblTotalDeduction.setText("" + totaldeduction);
            salaryAfter += Salary - totaldeduction;
            lblTotalAfterDedduction.setText(String.valueOf(salaryAfter));
        } else if (RadioAmount.isSelected() == true) {
            deduction = Integer.parseInt(txtRadioAmount.getText());
            totaldeduction = (deduction);

            lblTotalDeduction.setText(String.valueOf(totaldeduction));
            salaryAfter += Salary - totaldeduction;
            lblTotalAfterDedduction.setText(String.valueOf(salaryAfter));
        } else {
            JOptionPane.showMessageDialog(null, "Pleas check deduction mode");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

